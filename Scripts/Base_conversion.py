########################################################
# Binary

# Decimal to Binary
print("DECIMAL to BINARY",bin(15))

# octal to Binary
print("OCTAL to BINARY",bin(0o1234))

# hexadecimal to Binary
print("HEXADECIMAL to BINARY",bin(0xFace))

########################################################
# Octal

# Decimal to Octal
print("DECIMAL to OCTAL",oct(15))

# Binary to Octal
print("BINARY to OCTAL",oct(0b101))

# hexadecimal to Octal
print("HEXADECIMAL to OCTAL",oct(0xFace))

########################################################
# Hexadecimal

# Decimal to Hexadecimal
print("DECIMAL to HEXADECIMAL",hex(15))

# Binary to Hexadecimal
print("BINARY to HEXADECIMAL",hex(0b111))

# Octal to Hexadecimal
print("OCTAL to HEXADECIMAL",hex(0o1234))

