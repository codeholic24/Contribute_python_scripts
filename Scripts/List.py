# Example 6 : Element can be added to List
l=[10,20,10,40]
print("Data in List  : ", l)
# Output : Data in List  :  [10, 20, 10, 40]

l.append(54)
print("Data in List  : ", l)
# Output : Data in List  :  [10, 20, 10, 40, 54]


# Example 7 : Element can be removed from List
l=[10,20,10,40]
print("Data in List  : ", l)
# Output : Data in List  :  [10, 20, 10, 40]

l.remove(20)
print("Data in List  : ", l)
# Output : Data in List  :  [10, 10, 40]
